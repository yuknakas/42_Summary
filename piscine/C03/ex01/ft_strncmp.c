int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int				ans;
	unsigned int	i;

	i = 0;
	ans = 0;
	if (n == 0)
		return (0);
	while (i < n - 1 && s1[i] != '\0' && s2[i] != '\0' && s1[i] == s2[i])
		i++;
	ans = (unsigned char)s1[i] - (unsigned char)s2[i];
	return (ans);
}
