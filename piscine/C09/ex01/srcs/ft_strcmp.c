int	ft_strcmp(char *s1, char *s2)
{
	int	ans;

	ans = 0;
	while (*s1 && (*s1 == *s2))
	{
		s1++;
		s2++;
	}
	ans = (unsigned char)*s1 - (unsigned char)*s2;
	return (ans);
}
